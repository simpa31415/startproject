from flask import Flask, render_template
import os

app = Flask(__name__)

@app.route("/")
def home():
    return render_template("index.html")  # index.html ligger i templates/

if __name__ == "__main__":
    port = int(os.environ.get("PORT", 5000))  # Render s√§tter PORT som env-variable
    app.run(host="0.0.0.0", port=port)
